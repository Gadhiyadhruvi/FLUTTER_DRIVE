import 'dart:io';
void main(){
  int mark=70;
  if(mark>=70 && mark<=100){
    print("A Grade");
  }else if(mark>=60 && mark<=70){
    print("B Grade");
  }else if(mark>=50 && mark<=60){
    print("C Grade");
  }else if(mark>=40 && mark<=50){
    print("D Grade");
  }else{
    print("Fail");
  }
  
  
}